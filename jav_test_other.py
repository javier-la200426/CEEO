from arduino_alvik import ArduinoAlvik
from time import sleep
import sys

alvik = ArduinoAlvik()
alvik.begin()

alvik.left_wheel.reset()
alvik.right_wheel.reset()

while True:
    try:
        alvik.set_wheels_speed(60, 60)
        sleep(2)
        alvik.set_wheels_speed(0, 0)
        sleep(0.5)
        alvik.set_wheels_speed(-60, -60)
        sleep(2)

        alvik.set_wheels_speed(40, -40)
        sleep(2)
        alvik.set_wheels_speed(0, 0)
        sleep(0.5)
        
        alvik.set_wheels_speed(-40, 40)
        sleep(2)
        alvik.set_wheels_speed(0, 0)
        sleep(0.5)

        alvik.set_wheels_speed(-40, 40)
        sleep(0.5)
        
        alvik.set_wheels_speed(40, -40)
        sleep(0.5)

        alvik.set_wheels_speed(-40, 40)
        sleep(0.5)
        
        alvik.set_wheels_speed(40, -40)
        sleep(0.5)
        
        alvik.set_wheels_speed(-40, 40)
        sleep(0.5)
       
    except KeyboardInterrupt as e:
        print('over')
        alvik.stop()
        sys.exit()
